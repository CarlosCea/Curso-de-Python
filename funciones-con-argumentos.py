#Parametro es una variable definida entre los parentesis de la funcion
#Argumento (arg) es el valor enviado a la funcion

def funcion_arg(nombre, apellido):#parametro
    print("El nombre recibido es:", nombre)
    print("El apellido recibido es:", apellido)

funcion_arg("Juan","Perez")#argumento
funcion_arg("Karla","Gomez")